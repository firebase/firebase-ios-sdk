    @available(iOS 26.0, macOS 26.0, *)
    @available(tvOS, unavailable)
    @available(watchOS, unavailable)
    extension FoundationModels.GeneratedContent.Kind {
      func toFirebase(isComplete: Bool) -> FirebaseAI.GeneratedContent.Kind {
        switch self {
        case .null:
          return .null
        case let .bool(value):
          return .bool(value)
        case let .number(value):
          return .number(value)
        case let .string(value):
          return .string(value)
        case let .array(values):
          return .array(
            values.map { FirebaseAI.GeneratedContent(kind: $0.kind, id: nil, isComplete: isComplete)
            }
          )
        case let .structure(properties, orderedKeys):
          return .structure(
            properties: properties
              .mapValues {
                FirebaseAI.GeneratedContent(kind: $0.kind, id: nil, isComplete: isComplete)
              },
            orderedKeys: orderedKeys
          )
        @unknown default:
          assertionFailure("Unknown `FoundationModels.GeneratedContent` kind: \(self)")
          return .null
        }
      }
    }

