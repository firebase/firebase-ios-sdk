import Foundation

/// A type that represents a remote multimodal model (like Gemini), with the ability to generate
/// content based on various input types.
///
/// **Public Preview**: This API is a public preview and may be subject to change.
public final class TemplateGenerativeModel: Sendable {
  let generativeAIService: GenerativeAIService
  let apiConfig: APIConfig
  let generativeModel: GenerativeModel

  init(generativeModel: GenerativeModel, generativeAIService: GenerativeAIService,
       apiConfig: APIConfig) {
    self.generativeAIService = generativeAIService
    self.apiConfig = apiConfig
    self.generativeModel = generativeModel
  }

  /// Generates content from a prompt template and inputs.
  ///
  /// **Public Preview**: This API is a public preview and may be subject to change.
  ///
  /// - Parameters:
  ///   - templateID: The ID of the prompt template to use.
  ///   - inputs: A dictionary of variables to substitute into the template.
  ///   - options: The ``RequestOptions`` for the request, currently used to override default
  /// request timeout.
  /// - Returns: The content generated by the model.
  /// - Throws: A ``GenerateContentError`` if the request failed.
  public func generateContent(templateID: String,
                              inputs: [String: any Sendable],
                              options: RequestOptions = RequestOptions()) async throws
    -> GenerateContentResponse {
    return try await generateContent(
      template: templateID,
      inputs: inputs,
      history: [],
      options: options
    )
  }

  /// Generates content from a prompt template and inputs, with streaming responses.
  ///
  /// **Public Preview**: This API is a public preview and may be subject to change.
  ///
  /// - Parameters:
  ///   - templateID: The ID of the prompt template to use.
  ///   - inputs: A dictionary of variables to substitute into the template.
  ///   - options: The ``RequestOptions`` for the request, currently used to override default
  /// request timeout.
  /// - Returns: An `AsyncThrowingStream` that yields `GenerateContentResponse` objects.
  /// - Throws: A ``GenerateContentError`` if the request failed.
  @available(macOS 12.0, watchOS 8.0, *)
  public func generateContentStream(templateID: String,
                                    inputs: [String: Any],
                                    options: RequestOptions = RequestOptions()) throws
    -> AsyncThrowingStream<GenerateContentResponse, Error> {
    let templateInputs = try inputs.mapValues { try TemplateInput(value: $0) }
    let request = TemplateGenerateContentRequest(
      template: templateID,
      inputs: templateInputs,
      history: [],
      projectID: generativeAIService.firebaseInfo.projectID,
      stream: true,
      apiConfig: apiConfig,
      options: options
    )

    return try GenerativeModel.generateContentStream(service: generativeAIService, request: request)
  }

  // TODO: Restore `public` determined to be releasable along with the contents of TemplateChatSession.
  // TODO: Add `inputs` to `startChat` since they are fixed for the duration of the chat session.

  /// Creates a new chat conversation using this model with the provided history and template.
  ///
  /// - Parameters:
  ///   - templateID: The ID of the prompt template to use.
  ///   - history: The conversation history to use.
  /// - Returns: A new ``TemplateChatSession`` instance.
  func startChat(templateID: String, inputs: [String: any Sendable] = [:],
                 history: [ModelContent] = []) -> TemplateChatSession {
    return TemplateChatSession(
      model: self,
      templateID: templateID,
      inputs: inputs,
      history: history
    )
  }

  // MARK: - Internal

  func templateGenerateContentRequest(template: String,
                                      inputs: [String: Any], history: [ModelContent], stream: Bool,
                                      options: RequestOptions) throws
    -> TemplateGenerateContentRequest {
    try history.throwIfError()
    let templateInputs = try inputs.mapValues { try TemplateInput(value: $0) }

    return TemplateGenerateContentRequest(
      template: template,
      inputs: templateInputs,
      history: history,
      projectID: generativeAIService.firebaseInfo.projectID,
      stream: stream,
      apiConfig: apiConfig,
      options: options
    )
  }

  /// Generates content from a prompt template, inputs, and history.
  ///
  /// - Parameters:
  ///   - history: The conversation history to use.
  ///   - template: The prompt template to use.
  ///   - inputs: A dictionary of variables to substitute into the template.
  /// - Returns: The content generated by the model.
  /// - Throws: A ``GenerateContentError`` if the request failed.
  func generateContent(template: String, inputs: [String: Any], history: [ModelContent],
                       options: RequestOptions) async throws
    -> GenerateContentResponse {
    let request = try templateGenerateContentRequest(
      template: template,
      inputs: inputs,
      history: history,
      stream: false,
      options: options
    )

    return try await GenerativeModel.generateContent(service: generativeAIService, request: request)
  }

  @available(macOS 12.0, watchOS 8.0, *)
  func generateContent(history: [ModelContent], template: String, inputs: [String: Any],
                       options: RequestOptions) throws
    -> AsyncThrowingStream<GenerateContentResponse, Error> {
    let templateInputs = try inputs.mapValues { try TemplateInput(value: $0) }
    let request = TemplateGenerateContentRequest(
      template: template,
      inputs: templateInputs,
      history: history,
      projectID: generativeAIService.firebaseInfo.projectID,
      stream: true,
      apiConfig: apiConfig,
      options: options
    )

    return try GenerativeModel.generateContentStream(service: generativeAIService, request: request)
  }
}
