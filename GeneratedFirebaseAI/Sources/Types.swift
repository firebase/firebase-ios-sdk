  /// `SessionResmumptionTokenUpdate`. This field will enable them to limit buffering
  /// (avoid keeping all requests in RAM).
  ///
  /// Note: This should not be used for when resuming a session at some time later --
  /// in those cases partial audio and video frames arelikely not needed.
  public let lastConsumedClientMessageIndex: Int64?

  /// Default initializer.
  public init(
    newHandle: String? = nil,
    resumable: Bool? = nil,
    lastConsumedClientMessageIndex: Int64? = nil
