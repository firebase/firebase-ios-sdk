import Foundation


extension GeminiDataModels {
  /// An internal data model for `ExecutableCode`.
  /// 
  /// ### Gemini Developer API
  /// 
  /// Type: `ExecutableCode`
  /// 
  /// An element in the history that represents code generated by the model that is
  /// meant to be executed, and the result returned to the model.
  /// 
  /// ### Gemini Enterprise Agent Platform
  /// 
  /// Type: `GoogleCloudAiplatformV1beta1ExecutableCode`
  /// 
  /// Code generated by the model that is meant to be executed, and the result
  /// returned to the model.
  /// 
  /// Generated when using the `CodeExecution` tool, in which the code will
  /// be automatically executed, and a corresponding CodeExecutionResult will
  /// also be generated.
  package struct ExecutableCode: Codable, Sendable, Equatable, Hashable {
    /// Required. Programming language of the `code`.
    /// 
    /// ### Gemini Developer API
    /// 
    /// Required. Programming language of the `code`.
    /// String representation of a Gemini ExecutableCode.Language enum
    /// http://google3/google/ai/generativelanguage/v1main/content.proto?q=symbol:%5CbLanguage%5Cb
    /// 
    /// ### Gemini Enterprise Agent Platform
    /// 
    /// Required. Programming language of the `code`.
    package let language: String
    
    /// Required. The code to be executed.
    package let code: String
    
    /// Optional. Unique identifier of the `ExecutableCode` part.
    /// 
    /// ### Gemini Developer API
    /// 
    /// Optional. Unique identifier of the `ExecutableCode` part.
    /// The server returns the `CodeExecutionResult` with the matching `id`.
    /// 
    /// ### Gemini Enterprise Agent Platform
    /// 
    /// > Important: This property is not supported in the Gemini Enterprise Agent Platform.
    package let id: String?
    

    /// Creates a new `ExecutableCode`.
    ///
    /// - Parameters:
    ///   - language: Required. Programming language of the `code`. (behavior varies by backend). For more details, see ``language``.
    ///   - code: Required. The code to be executed.
    ///   - id: Optional. Unique identifier of the `ExecutableCode` part. (Gemini Developer API only). For more details, see ``id``.
    package init(
      language: String,
      code: String,
      id: String? = nil
    ) {
      self.language = language
      self.code = code
      self.id = id
    }
    enum CodingKeys: String, CodingKey {
      case language = "language"
      case code = "code"
      case id = "id"
    }
  }
}