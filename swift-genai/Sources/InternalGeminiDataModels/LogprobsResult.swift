import Foundation


extension GeminiDataModels {
  /// An internal data model for `LogprobsResult`.
  /// 
  /// ### Gemini Developer API
  /// 
  /// Type: `GoogleAiGenerativelanguageV1betaLogprobsResult`
  /// 
  /// Logprobs Result
  /// 
  /// ### Gemini Enterprise Agent Platform
  /// 
  /// Type: `GoogleCloudAiplatformV1beta1LogprobsResult`
  /// 
  /// The log probabilities of the tokens generated by the model.
  /// 
  /// This is useful for understanding the model's confidence in its predictions
  /// and for debugging. For example, you can use log probabilities to identify
  /// when the model is making a less confident prediction or to explore
  /// alternative responses that the model considered. A low log probability can
  /// also indicate that the model is "hallucinating" or generating factually
  /// incorrect information.
  package struct LogprobsResult: Codable, Sendable, Equatable, Hashable {
    /// Sum of log probabilities for all tokens.
    /// 
    /// ### Gemini Developer API
    /// 
    /// Sum of log probabilities for all tokens.
    /// 
    /// ### Gemini Enterprise Agent Platform
    /// 
    /// > Important: This property is not supported in the Gemini Enterprise Agent Platform.
    package let logProbabilitySum: Double?
    
    /// Length = total number of decoding steps.
    /// 
    /// ### Gemini Developer API
    /// 
    /// Length = total number of decoding steps.
    /// 
    /// ### Gemini Enterprise Agent Platform
    /// 
    /// A list of the top candidate tokens at each decoding step. The
    /// length of this list is equal to the total number of decoding steps.
    package let topCandidates: [TopCandidates]?
    
    /// Length = total number of decoding steps.
    /// 
    /// ### Gemini Developer API
    /// 
    /// Length = total number of decoding steps.
    /// The chosen candidates may or may not be in top_candidates.
    /// 
    /// ### Gemini Enterprise Agent Platform
    /// 
    /// A list of the chosen candidate tokens at each decoding step.
    /// The length of this list is equal to the total number of decoding steps.
    /// Note that the chosen candidate might not be in `top_candidates`.
    package let chosenCandidates: [LogprobsResultCandidate]?
    

    /// Creates a new `LogprobsResult`.
    ///
    /// - Parameters:
    ///   - logProbabilitySum: Sum of log probabilities for all tokens. (Gemini Developer API only). For more details, see ``logProbabilitySum``.
    ///   - topCandidates: Length = total number of decoding steps. (behavior varies by backend). For more details, see ``topCandidates``.
    ///   - chosenCandidates: Length = total number of decoding steps. (behavior varies by backend). For more details, see ``chosenCandidates``.
    package init(
      logProbabilitySum: Double? = nil,
      topCandidates: [TopCandidates]? = nil,
      chosenCandidates: [LogprobsResultCandidate]? = nil
    ) {
      self.logProbabilitySum = logProbabilitySum
      self.topCandidates = topCandidates
      self.chosenCandidates = chosenCandidates
    }
    enum CodingKeys: String, CodingKey {
      case logProbabilitySum = "logProbabilitySum"
      case topCandidates = "topCandidates"
      case chosenCandidates = "chosenCandidates"
    }
  }
}