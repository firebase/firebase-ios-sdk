import Foundation


extension GeminiDataModels {
  /// An internal data model for `HistoryPart`.
  /// 
  /// ### Gemini Developer API
  /// 
  /// Type: `HistoryPart`
  /// 
  /// A single message from the conversation history list. This can represent a
  /// plain text message, a request to invoke a client-side function or the output
  /// from invoking a client-side function.
  /// 
  /// ### Gemini Enterprise Agent Platform
  /// 
  /// Type: `HistoryPart`
  /// 
  /// A single message from the conversation history list. This can represent a
  /// plain text message, a request to invoke a client-side function or the output
  /// from invoking a client-side function.
  package struct HistoryPart: Codable, Sendable, Equatable, Hashable {
    /// Optional. A plain text message from one party in the conversation.
    package let text: String?
    
    /// Optional. A request to invoke a client-side function
    package let functionCall: FunctionCall?
    
    /// Optional. The output from a client-side function invocation
    package let functionResponse: FunctionResponse?
    
    /// Optional. Inline media bytes.
    package let inlineData: Blob?
    
    /// Optional. URI based data.
    package let fileData: FileData?
    
    /// Optional. Code generated by the model that is meant to be executed.
    package let executableCode: ExecutableCode?
    
    /// Optional. Result of executing the `ExecutableCode`.
    package let codeExecutionResult: CodeExecutionResult?
    
    /// Optional. Indicates if the part is thought from the model.
    package let thought: Bool?
    
    /// Optional. Represents an opaque signature for the thought so it can be reused in
    /// subsequent requests.
    package let thoughtSignature: String?
    

    /// Creates a new `HistoryPart`.
    ///
    /// - Parameters:
    ///   - text: Optional. A plain text message from one party in the conversation.
    ///   - functionCall: Optional. A request to invoke a client-side function
    ///   - functionResponse: Optional. The output from a client-side function invocation
    ///   - inlineData: Optional. Inline media bytes.
    ///   - fileData: Optional. URI based data.
    ///   - executableCode: Optional. Code generated by the model that is meant to be executed.
    ///   - codeExecutionResult: Optional. Result of executing the `ExecutableCode`.
    ///   - thought: Optional. Indicates if the part is thought from the model.
    ///   - thoughtSignature: Optional. Represents an opaque signature for the thought so it can be reused in
    package init(
      text: String? = nil,
      functionCall: FunctionCall? = nil,
      functionResponse: FunctionResponse? = nil,
      inlineData: Blob? = nil,
      fileData: FileData? = nil,
      executableCode: ExecutableCode? = nil,
      codeExecutionResult: CodeExecutionResult? = nil,
      thought: Bool? = nil,
      thoughtSignature: String? = nil
    ) {
      self.text = text
      self.functionCall = functionCall
      self.functionResponse = functionResponse
      self.inlineData = inlineData
      self.fileData = fileData
      self.executableCode = executableCode
      self.codeExecutionResult = codeExecutionResult
      self.thought = thought
      self.thoughtSignature = thoughtSignature
    }
    enum CodingKeys: String, CodingKey {
      case text = "text"
      case functionCall = "functionCall"
      case functionResponse = "functionResponse"
      case inlineData = "inlineData"
      case fileData = "fileData"
      case executableCode = "executableCode"
      case codeExecutionResult = "codeExecutionResult"
      case thought = "thought"
      case thoughtSignature = "thoughtSignature"
    }
  }
}