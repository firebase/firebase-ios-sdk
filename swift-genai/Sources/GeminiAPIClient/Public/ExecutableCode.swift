import Foundation
package import InternalGoogleAIDataModels
package import InternalAgentPlatformDataModels

/// Represents executable code generated by the model.
public struct ExecutableCode: Codable, Sendable, Equatable, Hashable {
  public var code: String?
  public var language: Language?
  /// - Note: Only supported on GoogleAI backend.
  public var id: String?

  public init(code: String? = nil, language: Language? = nil, id: String? = nil) {
    self.code = code
    self.language = language
    self.id = id
  }
}

public enum Language: Codable, Sendable, Equatable, Hashable {
  case python
  case unrecognized(_ value: String)
}

// MARK: - GoogleAI Mappings

extension ExecutableCode {
  package func toGoogleAI() -> GoogleAI.ExecutableCode {
    GoogleAI.ExecutableCode(code: code, id: id, language: language?.toGoogleAI())
  }

  package init(fromGoogleAI ec: GoogleAI.ExecutableCode) {
    self.code = ec.code
    self.language = ec.language.map { Language(fromGoogleAI: $0) }
    self.id = ec.id
  }
}

extension Language {
  package func toGoogleAI() -> GoogleAI.ExecutableCode.Language {
    switch self {
    case .python: .python
    case .unrecognized(let val): .unrecognized(val)
    }
  }

  package init(fromGoogleAI lang: GoogleAI.ExecutableCode.Language) {
    switch lang {
    case .python: self = .python
    case .unrecognized(let val): self = .unrecognized(val)
    }
  }
}

// MARK: - AgentPlatform Mappings

extension ExecutableCode {
  package func toAgentPlatform() -> AgentPlatform.ExecutableCode {
    AgentPlatform.ExecutableCode(code: code, language: language?.toAgentPlatform())
  }

  package init(fromAgentPlatform ec: AgentPlatform.ExecutableCode) {
    self.code = ec.code
    self.language = ec.language.map { Language(fromAgentPlatform: $0) }
    self.id = nil
  }
}

extension Language {
  package func toAgentPlatform() -> AgentPlatform.ExecutableCode.Language {
    switch self {
    case .python: .python
    case .unrecognized(let val): .unrecognized(val)
    }
  }

  package init(fromAgentPlatform lang: AgentPlatform.ExecutableCode.Language) {
    switch lang {
    case .python: self = .python
    case .unrecognized(let val): self = .unrecognized(val)
    }
  }
}
